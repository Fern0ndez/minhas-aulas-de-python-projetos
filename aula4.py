#Tipos int e float
#Int -> número inteiro
#O tipo representa qualquer número inteiro, positivo ou negativo, sem parte decimal. Exemplos: 1234, -1234, 0, 999999
#positivo ou negativo. int sem sinal é considerado positivo.
print(11) # int 
print(-11) # int 
print(0)

#float -> número de ponto flutuante
#O tipo float representa qualquer número
#Positivo ou negativo com ponto flutuante. Exemplos: 1.1, -1.1, 0.0, 3.14, -3.14
#float sem sinal e'considerado positivo.
print(1.1, 10.11)   
print(0.0,  -1.5)

#A função type mostra o tipo que o python
#inferiu ao valor
print(      type(1)       )
print(      type("Gabriel"),type(1.0),type(-1)       )
