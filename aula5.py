# Tipo de dado bool (boolean)
# Ao questionar algo em um programa,
# só existem duas respostas possíveis:
# sim (true) ou não (false).
# Existem vários operadores para "questionar".
# Dentre eles o ==, queé um operador logico que
# questiona se o valor da esquerda é igual a outro
print(10 == 10) # sim => true (verdadeiro)
print(10 == 11) # não => false(falso)
print(type(10 == 10)) 
print(type(10 == 11))
print(type(True))
print(type(False))
# se por o comando do true e false do bool dentro do parentese do comando type ele vai ler como bool
